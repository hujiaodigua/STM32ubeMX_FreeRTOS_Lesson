/* MailProducerTask function */
void MailProducerTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
		Amail_TypeDef Mail[3];
		
		Mail[0].var1 = 0x01; /*设置邮件内容*/
		Mail[0].var2 = 0x02;
		Mail[0].var3 = 0x03;
		
		Mail[1].var1 = 0x02;
		Mail[1].var2 = 0x03;
		Mail[1].var3 = 0x04;
		
		Mail[2].var1 = 0x03;
		Mail[2].var2 = 0x04;
		Mail[2].var3 = 0x05;
  /* Infinite loop */
  for(;;)
  {
		osMailPut(mailQ01Handle, &Mail[0]); //发送邮件
    osDelay(1000);
		osMailPut(mailQ01Handle, &Mail[1]); //发送邮件
    osDelay(1000);
		osMailPut(mailQ01Handle, &Mail[2]); //发送邮件
    osDelay(3000);
  }
  /* USER CODE END 5 */ 
}

/* MailConsumerTask function */
void MailConsumerTask(void const * argument)
{
  /* USER CODE BEGIN MailConsumerTask */
	osEvent event;
	Amail_TypeDef *pMail;
	uint32_t cnt;
  /* Infinite loop */
  for(;;)
  {
		event = osMailGet(mailQ01Handle, osWaitForever); //获取邮件
		if(event.status == osEventMail)
		{
			pMail = event.value.p; //获取邮件指针
			cnt = pMail -> var1; //提取邮件中的数据var2（当然也可以是其它数据）
			while(cnt--) //根据邮箱中传来的数据var2执行递减循环控制LED灯
			{							//0x01就是控制LED闪一次，0x02就是闪两次
										//证明两个进程间进行了非常有效的通信
				HAL_GPIO_WritePin( LED0_GPIO_Port, LED0_Pin, GPIO_PIN_RESET);	//LED0亮
				HAL_GPIO_WritePin( LED1_GPIO_Port, LED1_Pin, GPIO_PIN_RESET);	//LED1亮
				osDelay(100);
				
				HAL_GPIO_WritePin( LED0_GPIO_Port, LED0_Pin, GPIO_PIN_SET);		//LED0灭
				HAL_GPIO_WritePin( LED1_GPIO_Port, LED1_Pin, GPIO_PIN_SET);		//LED1灭	
				osDelay(100);		
			}
		}
    osDelay(1);
  }
  /* USER CODE END MailConsumerTask */
}

